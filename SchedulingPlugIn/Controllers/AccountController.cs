        public ActionResult ForgotPassword(ForgotPasswordViewModel model)
        {
            if (ModelState.IsValid)
            {
                var user = UserManager.FindByName(model.Email);
                if (user == null || !(UserManager.IsEmailConfirmed(user.Id)))
                {
                    // Don't reveal that the user does not exist or is not confirmed
                    ModelState.AddModelError("Email", "Email is not exist.");
                    return View(model);
                }
                else
                {
                    ApplicationDbContext context = new ApplicationDbContext();
                    var mailUser = context.Users.FirstOrDefault(e => e.Email == model.Email || e.UserName == model.Email);
                    if (mailUser == null)
                    {
                        ModelState.AddModelError("Email", "Username or Email is not exist.");
                        return View(model);
                    }
                }

                // For more information on how to enable account confirmation and password reset please visit https://go.microsoft.com/fwlink/?LinkID=320771
                // Send an email with this link
                string code = UserManager.GeneratePasswordResetToken(user.Id);
                var callbackUrl = Url.Action("ResetPassword", "Account", new { userId = user.Id, code = code, email = user.Email}, protocol: Request.Url.Scheme);
                sendMail(model, callbackUrl);
                //UserManager.SendEmailAsync(user.Id, "Reset Password", "Please reset your password by clicking <a href=\"" + callbackUrl + "\">here</a>");
                return RedirectToAction("ForgotPasswordConfirmation", "Account");
            }
            // If we got this far, something failed, redisplay form
            return View(model);
        }
